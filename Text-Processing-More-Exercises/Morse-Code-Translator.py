morse = input().split(" ")
dect = {"..": "I", ".-": "A", "-...": "B", "-.-.": "C", "-..": "D", '.': "E", "..-.": "F", "--.": "G", "....": "H", ".---": "J", "-.-": "K", ".-..": "L", "--": "M", "-.": "N", "---": "O",
        ".--.": "P", "--.-": "Q", ".-.": "R", "...": "S", "-": "T", "..-": "U", "...-": "V", ".--": "W", "-..-": "X", "-.--": "Y", "--..":"Z"}
result = ""
for code in morse:
    if code != "":
        if code == "|":
            result+= " "
        else:
            result+=dect[code]
print(result)
